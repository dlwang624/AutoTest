package org.czy.util;

public class GetMaxOrMin {
	 public static void main(String args[])
	 {
		 int A[]={74,48,30,17,62};  // 声明整数数组A,并赋初值
	
		 getArrayMax(A);
	}
	 
	 public static int getArrayMax(int[] A){
		 int i,max;
		 max=A[0];
		 for(i=0;i<A.length;i++)
		 {
//			 System.out.print(A[i]+" ");
			 if(A[i]>max){   // 判断最大值
			 max=A[i];
			 }
		 }
//		 System.out.println("\n数组的最大值是："+max); // 输出最大值
		 return max;
	 }
	 
	 public static int getArrayMin(int[] A){
		 int i,min;
		 min=A[0];
		 for(i=0;i<A.length;i++)
		 {
			 System.out.print(A[i]+" ");
			 if(A[i]<min){   // 判断最小值
			 min=A[i];
			 }
		 }
//		 System.out.println("数组的最小值是："+min); // 输出最小值
		 return min;
	 }
	 
	 public static int getArrayMin(String[] A){
		 int i,min;
		 min=Integer.valueOf(A[0]);
		 for(i=0;i<A.length;i++)
		 {
			 System.out.print(A[i]+" ");
			 if(Integer.valueOf(A[i])<min){   // 判断最小值
			 min=Integer.valueOf(A[i]);
			 }
		 }
//		 System.out.println("数组的最小值是："+min); // 输出最小值
		 return min;
	 }
	 
	 public static int getArrayMax(String[] A){
		 int i,max;
		 max=Integer.valueOf(A[0]);
		 for(i=0;i<A.length;i++)
		 {
//			 System.out.print(A[i]+" ");
			 if(Integer.valueOf(A[i])>max){   // 判断最大值
			 max=Integer.valueOf(A[i]);
			 }
		 }
//		 System.out.println("\n数组的最大值是："+max); // 输出最大值
		 return max;
	 }
}
